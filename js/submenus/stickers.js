    name: 'Kid Pix Sticker 1',
    imgSrc: './src/assets/img/kp-sticker-1.png',
    handler: function (e) {
        var img = new Image();
        img.src = './src/assets/img/kp-sticker-1.png';
        img.crossOrigin = 'anonymous';
        img.onload = function () {
            KiddoPaint.Tools.Placer.image = img;
            KiddoPaint.Tools.Placer.size = {
                width: img.width,
                height: img.height
            };
            KiddoPaint.Current.tool = KiddoPaint.Tools.Placer;
        };
    }
